  @override
  void initState() {
    super.initState();
    _tabController = TabController(length: _categories.length, vsync: this);
    _tabController.addListener(() {
      if (mounted) setState(() => _selectedCategory = _tabController.index);
    });
  }

  @override
  void dispose() {
    _tabController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return Container(
      height: 280.h,
      decoration: BoxDecoration(
        color: Colors.black.withValues(alpha: 0.92),
        borderRadius: BorderRadius.vertical(top: Radius.circular(20.r)),
        border: Border(
          top: BorderSide(color: Colors.white.withValues(alpha: 0.1)),
        ),
      ),
      child: Column(
        children: [
          // Category tab bar
          SizedBox(
            height: 48.h,
            child: TabBar(
              controller: _tabController,
              isScrollable: true,
              dividerColor: Colors.transparent,
              indicatorColor: Colors.white,
              indicatorWeight: 2,
              labelPadding: EdgeInsets.symmetric(horizontal: 14.w),
              tabs: _categories
                  .map(
                    (c) => Tab(
                      child: Text(c.icon, style: TextStyle(fontSize: 22.sp)),
                    ),
                  )
                  .toList(),
            ),
          ),
          // Emoji grid
          Expanded(
            child: TabBarView(
              controller: _tabController,
              children: _categories.map((category) {
                return GridView.builder(
                  padding: EdgeInsets.all(8.w),
                  gridDelegate: SliverGridDelegateWithFixedCrossAxisCount(
                    crossAxisCount: 8,
                    mainAxisSpacing: 4.h,
                    crossAxisSpacing: 4.w,
                  ),
                  itemCount: category.emojis.length,
                  itemBuilder: (context, index) {
                    return GestureDetector(
                      onTap: () =>
                          widget.onEmojiSelected(category.emojis[index]),
                      child: Center(
                        child: Text(
                          category.emojis[index],
                          style: TextStyle(fontSize: 26.sp),
                        ),
                      ),
                    );
                  },
                );
              }).toList(),
            ),
          ),
        ],
      ),
    );
  }
